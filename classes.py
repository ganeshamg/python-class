# Define a class named Car
class Car:
    # Class attribute (shared by all instances)
    wheels = 4

    # Constructor to initialize the car's attributes
    def __init__(self, make, model, year):
        # Instance attributes (specific to each instance)
        self.make = make
        self.model = model
        self.year = year

    # Method to describe the car
    def car_info(self):
        return f"{self.year} {self.make} {self.model}"

# Create an instance (object) of the Car class
my_car = Car("Toyota", "Camry", 2020)

# Access the attributes and call the method
print(my_car.car_info())  # Output: 2020 Toyota Camry
print(my_car.wheels)      # Output: 4 (shared class attribute)


# Create another instance of Car
another_car = Car("Honda", "Civic", 2019)

# Call method and access attributes for another object
print(another_car.car_info())  # Output: 2019 Honda Civic


